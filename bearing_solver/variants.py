"""
Таблица всех 30 вариантов курсовых работ.
Вариант N = комбинация (геометрия, тип углубления):
  - Варианты 1–10:  геометрия A, типы 1–10
  - Варианты 11–20: геометрия B, типы 1–10
  - Варианты 21–30: геометрия C, типы 1–10
"""

import numpy as np

# ---------- Общие параметры ----------
n_rpm = 2980                          # об/мин
omega = 2 * np.pi * n_rpm / 60       # рад/с
eta = 0.01105                         # Па·с (SAE 30 при 60 °C)

N_phi = 8                             # углублений по окружности
N_Z = 11                              # углублений по длине
phi_start_deg = 90                    # начало зоны углублений, градусы
phi_end_deg = 270                     # конец зоны углублений, градусы

# ---------- Три геометрии подшипника ----------
GEOMETRIES = {
    "A": {"R": 0.035, "c": 0.00005, "L": 0.056},
    "B": {"R": 0.050, "c": 0.00007, "L": 0.080},
    "C": {"R": 0.025, "c": 0.00003, "L": 0.040},
}

# ---------- Десять типов микрорельефа ----------
DEPRESSION_TYPES = {
    1:  {"name": "Эллипсоидальный",           "a": 0.00241,  "b": 0.002214, "h_p": 10e-6},
    2:  {"name": "Параболический",             "a": 0.00250,  "b": 0.002300, "h_p": 10e-6},
    3:  {"name": "Цилиндрический",             "a": 0.00240,  "b": 0.002200, "h_p": 10e-6},
    4:  {"name": "Эллипт. цилиндр со скругл.", "a": 0.00245,  "b": 0.002250, "h_p": 10e-6},
    5:  {"name": "Сферическая шапка",          "r0": 0.00230,                "h_p": 12e-6},
    6:  {"name": "Конический",                 "a": 0.00235,  "b": 0.002150, "h_p": 10e-6},
    7:  {"name": "Суперэллиптический",         "a": 0.00255,  "b": 0.002350, "h_p": 10e-6},
    8:  {"name": "Плоскодонный с фаской",      "a": 0.00242,  "b": 0.002220, "h_p": 10e-6},
    9:  {"name": "Комбинированный",            "a": 0.00248,  "b": 0.002280, "h_p": 10e-6},
    10: {"name": "Трапецеидальный (прямоуг.)", "a": 0.00238,  "b": 0.002180, "h_p": 10e-6},
}


def get_variant(variant_number: int) -> dict:
    """Возвращает полный набор параметров для варианта 1..30."""
    if not 1 <= variant_number <= 30:
        raise ValueError("Номер варианта должен быть от 1 до 30")

    # Определяем геометрию и тип углубления
    geom_index = (variant_number - 1) // 10          # 0, 1 или 2
    dep_type = (variant_number - 1) % 10 + 1         # 1..10
    geom_key = ["A", "B", "C"][geom_index]

    geom = GEOMETRIES[geom_key]
    dep = DEPRESSION_TYPES[dep_type]

    R = geom["R"]
    c = geom["c"]
    L = geom["L"]

    U = omega * (R - c)

    # Безразмерные параметры углубления
    h_p = dep["h_p"]
    H_p = h_p / c

    if dep_type == 5:
        # Сферическая шапка: a = b = r0
        r0 = dep["r0"]
        a = r0
        b = r0
    else:
        a = dep["a"]
        b = dep["b"]

    A_nd = 2 * a / L          # безразмерная полуось по Z
    B_nd = b / R              # безразмерная полуось по φ

    return {
        "variant_number": variant_number,
        "geometry_key": geom_key,
        "depression_type": dep_type,
        "depression_name": dep["name"],
        "R": R,
        "c": c,
        "L": L,
        "U": U,
        "eta": eta,
        "omega_rad": omega,
        "h_p": h_p,
        "H_p": H_p,
        "a": a,
        "b": b,
        "A_nd": A_nd,
        "B_nd": B_nd,
        "N_phi": N_phi,
        "N_Z": N_Z,
        "phi_start": np.deg2rad(phi_start_deg),
        "phi_end": np.deg2rad(phi_end_deg),
    }
